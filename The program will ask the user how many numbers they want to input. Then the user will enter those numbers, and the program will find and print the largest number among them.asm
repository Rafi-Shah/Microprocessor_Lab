.MODEL SMALL
.STACK 100H
.DATA
COUNT DB ?
NUM DB ?
MAX DB 0

MSG1 DB "How many numbers: $"
MSG2 DB 0DH,0AH,"Enter number: $"
MSG3 DB 0DH,0AH,"Largest number is: $"

.CODE
MAIN PROC
MOV AX, @DATA
MOV DS, AX

MOV DX, OFFSET MSG1
MOV AH, 9
INT 21H

MOV AH, 1
INT 21H
SUB AL, 30H
MOV COUNT, AL

XOR CX, CX
MOV CL, COUNT

MOV MAX, 0

INPUT_LOOP:
MOV DX, OFFSET MSG2
MOV AH, 9
INT 21H

MOV AH, 1
INT 21H
SUB AL, 30H
MOV NUM, AL

MOV AL, NUM
CMP AL, MAX
JLE SKIP
MOV MAX, AL

SKIP:
LOOP INPUT_LOOP

MOV DX, OFFSET MSG3
MOV AH, 9
INT 21H

MOV DL, MAX
ADD DL, 30H
MOV AH, 2
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
