.MODEL SMALL
.STACK 100H

.DATA
A DB 5 DUP(?)
SUM DB 0
MAX DB ?
MIN DB ?

STR1 DB "Enter 5 NUMBERS: $"
STR2 DB "SUM OF THE NUMBERS: $"
STR3 DB "MAX NUMBER: $"
STR4 DB "MIN NUMBER: $"

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV DX,OFFSET STR1
MOV AH,9
INT 21H
CALL NEWLINE

MOV CX,5
MOV SI,0

INPUT:
MOV AH,1
INT 21H
SUB AL,30H
MOV A[SI],AL
INC SI
LOOP INPUT

CALL NEWLINE

CALL SUM1 ; SUM calculation
CALL MAX1 ; MAX calculation
CALL MIN1 ; MIN calculation

; ------- PRINT SUM -------
MOV DX,OFFSET STR2
MOV AH,9
INT 21H

MOV DL,SUM
ADD DL,30H
MOV AH,2
INT 21H
CALL NEWLINE

; ------- PRINT MAX -------
MOV DX,OFFSET STR3
MOV AH,9
INT 21H

MOV DL,MAX
ADD DL,30H
MOV AH,2
INT 21H
CALL NEWLINE

; ------- PRINT MIN -------
MOV DX,OFFSET STR4
MOV AH,9
INT 21H

MOV DL,MIN
ADD DL,30H
MOV AH,2
INT 21H
CALL NEWLINE

MOV AH,4CH
MOV AL,0
INT 21H

MAIN ENDP


; ---------- NEWLINE ----------
NEWLINE PROC
MOV AH,2
MOV DL,13
INT 21H
MOV DL,10
INT 21H
RET
NEWLINE ENDP


; ---------- SUM PROCEDURE ----------
SUM1 PROC
MOV AL,0
MOV CX,5
MOV SI,0

LOOP1:
ADD AL,A[SI]
INC SI
LOOP LOOP1

MOV SUM,AL
RET
SUM1 ENDP


; ---------- MAX PROCEDURE ----------
MAX1 PROC
MOV SI,0
MOV AL,A[SI] ; First element as MAX
MOV MAX,AL
INC SI

MOV CX,4

FIND_MAX:
MOV BL,A[SI]
CMP BL,MAX
JLE SKIP
MOV MAX,BL
SKIP:
INC SI
LOOP FIND_MAX

RET
MAX1 ENDP


; ---------- MIN PROCEDURE ----------
MIN1 PROC
MOV SI,0
MOV AL,A[SI] ; First element as MIN
MOV MIN,AL
INC SI

MOV CX,4

FIND_MIN:
MOV BL,A[SI]
CMP BL,MIN
JGE SKIP2
MOV MIN,BL
SKIP2:
INC SI
LOOP FIND_MIN

RET
MIN1 ENDP
